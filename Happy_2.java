public class Happy_2 {
    public static void main(String[] args) {
        int a;
        int b = 11111;
        a = 11;
        char gg = 'k';
        boolean isTrue = true;
        var d = 'h';
        double kia = 7.1234;
        System.out.println(a);
        System.out.println(b);
        System.out.println(d);
        System.out.printf("%c\n" , gg);
        System.out.printf("%b\n" , isTrue);
        System.out.printf("%7d\n" , b);
        System.out.printf("%-7d\n" , b);
        System.out.printf("%07d\n" , b);
        System.out.printf("%,7d\n" , b);
        System.out.printf("%f\n" , kia);
    }
}
